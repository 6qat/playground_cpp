#include <iostream>

using std::cout;
using std::endl;

int main() {

	cout << "srp.cpp : Single Responsibility Principle (separation of concerns)" << endl;
	cout << "ocp.cpp : Open-Closed Principle (classes open for extensions, closed for modifications)" << endl;
	cout << "lsp.cpp : Liskov Substitution Principle (able to substitute a base type for a subtype)" << endl;
	cout << "isp.cpp : Interface Segregation Principle (don't put too much into a single interface, split into many separate interfaces)" << endl;
	cout << "dip.cpp : Dependence Inversion Principle (high-level modules should not depend upon low-level ones, use abstractions)" << endl;

	return 0;
}